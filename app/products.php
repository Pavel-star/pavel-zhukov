самой разной сложности.</p>
<p>Предлагаем заказать кружки с логотипом, слоганом и названием компании, которые станут прекрасным корпоративным сувениром. Использовать такой приятный презент можно в качестве 
подарка партнерам и клиентам к Новому году, 8 марта, 23 февраля, ко дню рождения фирмы или профессиональному празднику.</p>
<p><img src="uploads/image-from-content.jpg" class="w-100" /></p>
<h2 class="content__title  text-left">Виды сувенирной продукции</h2>
<p>Мы выполняем печать фото на кружках, которые импортируем своими силами, а не закупаем у посредников. Этим объясняется доступная стоимость. Секрет яркого привлекательного изделия – 
не только в современном оборудовании для нанесения изображения, но также и в качественных заготовках.</p>
<p><img src="uploads/image-from-content_2.jpg" class="w-100" /></p>
<p>Мы используем кружки, которые относятся к классу А и отличаются следующими достоинствами:<br />
яркое белоснежное покрытие без каких-либо дополнительных оттенков, на котором будут прекрасно 
смотреться любые цвета;<br />
правильная геометрическая форма изделий.</p>
<h2 class="content__title  text-left">Преимущества печати на кружках в MUGforPRINT</h2>
<p>У нас вы можете заказать печать на кружках в любом количестве экземпляров. Небольшие партии 
и штучные заказы печатаются на территории нашего офиса в Москве. Работа может быть выполнена 
прямо при вас – не нужно будет тратить время на долгое ожидание. Печать на кружках в крупных 
партиях осуществляется на нашем производстве, способном справляться с нагрузкой до 5000 
экземпляров в сутки.</p>
